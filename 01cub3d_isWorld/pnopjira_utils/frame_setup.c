int	set_player(t_pos *p, int mx, int my, int ms, int maps)
{
	t_point	*pos;
	t_point	*mapp;

	pos = (*p).pos;
	mapp = (*p).mapp;
	(*p).ms = ms;
	set_point(mapp, mx, my, ms);
	set_point(pos, (mapp->x * maps) - (maps / 2) - (ms / 2), \
	(mapp->y * maps) - (maps / 2) - (ms / 2), ms);
	return (EXIT_SUCCESS);
}

int	init_frame(t_frame *scene)
{
	t_frame	*s;
	t_map	*m;
	t_pos	*p;

	s = scene;
	m = (*s).map;
	p = (*s).p;
	s->w = 1440;
	s->h = 810;
	m->mapx = 12;
	if (mapsize(m))
		return (4);
	if (m->maps == 18)
		p->ms = 6;
	else if (m->maps == 36)
		p->ms = 12;
	set_player(p, 1, 1, p->ms, m->maps);
	m->map = map;
	return (EXIT_SUCCESS);
}
